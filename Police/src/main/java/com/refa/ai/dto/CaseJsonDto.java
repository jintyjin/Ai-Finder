package com.refa.ai.dto;

public class CaseJsonDto {
	private String case_idx;
	private String case_json;
	private String row_num;
	
	public String getCase_idx() {
		return case_idx;
	}
	public void setCase_idx(String case_idx) {
		this.case_idx = case_idx;
	}
	public String getCase_json() {
		return case_json;
	}
	public void setCase_json(String case_json) {
		this.case_json = case_json;
	}
	public String getRow_num() {
		return row_num;
	}
	public void setRow_num(String row_num) {
		this.row_num = row_num;
	}

	
	
}
